        PriorityQueue<Integer> priorityQueue = new PriorityQueue<>();
        for (ListNode list : lists) {
            while(list!=null){
                priorityQueue.offer(list.val);
                list=list.next;
            }
        }
        ListNode pre = new ListNode();
